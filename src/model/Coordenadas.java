package model;

public class Coordenadas {

	double latiutud;
	double longitud;
	double altura;
	public double getLatiutud() {
		return latiutud;
	}
	public void setLatiutud(double latiutud) {
		this.latiutud = latiutud;
	}
	public double getLongitud() {
		return longitud;
	}
	public void setLongitud(double longitud) {
		this.longitud = longitud;
	}
	public double getAltura() {
		return altura;
	}
	public void setAltura(double altura) {
		this.altura = altura;
	}
	
	
	
	
	
}
